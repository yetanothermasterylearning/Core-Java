public class UnaryExample {

	public static void main(String[] args) {
		int a = 10;
		int b = 5;
		System.out.println(a++);
		System.out.println(a);
		System.out.println(++a);
		System.out.println(b--);
		System.out.println(b);
		System.out.println(--b);
		int c = 10;
		int d = 20;
		d += c;
		System.out.println(d);
	}

}
