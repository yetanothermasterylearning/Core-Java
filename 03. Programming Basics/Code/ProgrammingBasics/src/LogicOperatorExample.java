public class LogicOperatorExample {

	public static void main(String[] args) {
		boolean a = true;
		boolean b = true;
		System.out.println("AND operation:");
		System.out.println(a && b);
		System.out.println(a && !b);
		System.out.println(!a && b);
		System.out.println(!a && !b);
		
		System.out.println();
		
		System.out.println("OR operation:");
		System.out.println(a && b);
		System.out.println(a && !b);
		System.out.println(!a && b);
		System.out.println(!a && !b);
	}

}
